    public boolean isValidSudoku(char[][] board) {

        if(board.length != 9 || board[0].length != 9){
            return false;
        }

        int[] res;

        // row
        for(int i = 0; i < 9; i++){
            res = new int[9];
            for(int j = 0; j < 9; j++){
                if(board[i][j] == '.'){
                    continue;
                }
                int v = board[i][j] - '0';
                if(res[v-1] > 0){
                    return false;
                }else{
                    res[v-1] = 1;
                }
            }
        }

        // column
            for(int i = 0; i < 9; i++){
                res = new int[9];
                for(int j = 0; j < 9; j++){

                    if(board[j][i] == '.'){
                        continue;
                    }
                    int v = board[j][i] - '0';
                    if(res[v-1] > 0){
                        return false;
                    }else{
                        res[v-1] = 1;
                    }
                }
            }
        // 3 x 3
        for(int i = 0; i < 9; i += 3){
            for(int j = 0; j < 9; j += 3){
                res = new int[9];

                for(int p = 0; p < 3; p++){
                    for(int q = 0; q < 3; q++){
                        if(board[i+p][j+q] == '.'){
                            continue;
                        }
                        int v = board[i+p][j+q] - '0';
                        if(res[v-1] > 0){
                            return false;
                        }else{
                            res[v-1] = 1;
                        }
                    }
                }



            }
        }

        return true;
    }
